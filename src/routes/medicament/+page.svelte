<section class="medicament">
	<h2>HALTE AUX SUPPOSITOIRES !</h2>

	<p>
		De nombreux parents, médecins, soignants, continuent à administrer, prescrire des
		suppositoires d'antipyrétiques et antalgiques. Pourtant ceux-ci ont depuis plusieurs années
		démontré leur insuffisance d'efficacité. Pourquoi continuent-ils à être prescrits ?
	</p>

	<ul>
		<li>par habitude et commodité;</li>
		<li>
			parce qu'on continue à vivre sur d'anciennes croyances fausses : « ça marche plus vite
			et mieux » ;
		</li>
		<li>parce qu'on anticipe le refus de certains enfants d'avaler des médicaments;</li>
		<li>par méconnaissance des données récentes.</li>
	</ul>

	<ul class="dots">
		<li>
			La plupart des études qui ont essayé de mettre en évidence l'efficacité antalgique du
			paracétamol administré par cette voie sont négatives [7-11].
		</li>
		<li>
			Même si quelques travaux ont montré une égalité de l'effet antipyrétique du paracétamol
			par voie orale ou rectale, pour ce qui concerne l'effet antalgique, les études sont
			toujours en faveur de la voie orale [12].
		</li>
		<li>
			Des calculs de pharmacocinétique montrent que pour atteindre, des la première dose, une
			concentration cible de 9-18 mg/L, il faut administrer une dose de charge de 50 mg/kg
			suivie de 30 mg/kg toutes les 6 heures [13, 14]. Du fait de la considérable variabilité
			individuelle, ces posologies bien supérieures à celles qui sont recommandées, à la fois
			par l'AMM et les données de la littérature, ne peuvent être préconisées en pratique
			clinique : elles sont susceptibles de conduire chez certains enfants à un surdosage.
		</li>
		<li>
			Aujourd'hui les recommandations sont d'éviter l'administration par suppositoires, sauf
			vomissements incoercibles et absence de voie veineuse [15].
		</li>
	</ul>

	<h2>Des croyances surprenantes</h2>

	<p>
		Une idée reçue qui circule couramment garantit que le sens d'administration des
		suppositoires influe sur leur efficacité. Cette idée absurde n'a bien sûr aucun fondement
		rationnel. Enfin couper les suppositoires en deux est une pratique courante, mais les
		laboratiores pharmaceutiques ne peuvent garantir la répartition homogène de la substance
		active, sauf si le suppositoire est sécable.
	</p>
</section>

